# Python Bangla Tutorials 9 : Area of any shape
'''
Rectangle
Area = w * h
w = width
h = height
'''
width = float(input("Enter Width : "))
height = float(input("Enter Height : "))

area = width * height

print("Rectangle Area = ",area)




# Parallelogram

'''
area = b * h
b = base
h = height
'''

base = float(input("Enter Base : "))
height = float(input("Enter Height : "))

area = base * height

print("Parallelogram Area = ",area)
