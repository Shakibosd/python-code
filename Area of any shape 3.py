# Python Bangla Tutorials 9 : Area of any shape
'''
Square
Area = a^2
a = Length of side
'''
Lengthofside = float(input("Enter Length of side : "))

area = Lengthofside * Lengthofside

print("Square Area = ",area)





# Sector

'''
sector
Area = 0.5 * r^2 * θ
r = radius
θ = angle in radians
'''
radius = float(input("Enter Radius : "))
angleInRadians = float(input("Enter angle in radians : "))

area = radius * radius * angleInRadians

print("Sector Area = ",area)


