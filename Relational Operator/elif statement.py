# Python Bangla Tutorials 15 : elif statement

mark = 20

if mark>=80:
    print("Grade = ","A+")
    print("Congratulations !!!")

elif mark>=70:
    print("Grade = ","A")
    print("Congratulations !!!")

elif mark>=60:
    print("Grade = ","A-")
    print("Must do better")

elif mark>=50:
    print("Grade = ","B")
    print("Must do better")

elif mark>=40:
    print("Grade = ","C")
    print("Must do better")

elif mark>=33:
    print("Grade = ","D")
    print("Must do better")

else:
    print("Grade = ","Fail")
    print("Try Again")