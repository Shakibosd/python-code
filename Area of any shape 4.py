# Python Bangla Tutorials 9 : Area of any shape
'''
Ellipse
Area = 3.1416 * a * b
'''
area = float(input("Enter area : "))
base = float(input("Enter base : "))

result = 3.1416 * area * base

print("Ellipse Area = ",result)




# Trapezoid(us)
# Trapezium(uk)
'''
Area = 0.5(a+b) * h
h = vertical height
'''
area = float(input("Enter area : "))
height = float(input("Enter height : "))
base = float(input("Enter base : "))

result = 0.5 * (area+base) * height

print("trapezoid(us) Area = ",result)


